# While loops 
count = 0 
while count < 5:
    print("count is:", count)
    count += 1 

# this will print numbers 0 to 4 
    

# For loops 
pokemon_list = [ 'Pikachu', 'charizard', 'Infernape']
for pokemon in pokemon_list: 
    print(pokemon)

# this will print out each pokemon in the list
    

# Range ()
for i in range(1, 6):
    print(i)

# this will iterate ferom 1 to 5 

